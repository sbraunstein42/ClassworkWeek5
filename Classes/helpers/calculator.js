export class Calculator {

    constructor() {
        //don't need any parameters! so we can just leave it empty.
    }

    add(a, b) {
        return a + b;
    }

    subtract(a, b) {
        return a - b;
    }

    multiply(a, b) {
        return a * b;
    }

    divide(a, b) {
        return a / b;
    }


}